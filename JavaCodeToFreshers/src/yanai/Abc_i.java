
// Program serial 7(a)
// Finding sum of 1 to 10 numbers by using for loop (without formula)

package yanai;
public class Abc_i 
{
	public static void main(String [] args)
	
	{
	int sum = 0;
	for (int duck = 1; duck<=10 ; duck ++ )
	sum = sum + duck;
	System.out.println("The sum of 1 to 10 numbers is:" + sum);
	}
 
}
	
	


